#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <math.h>
#include <time.h>

#define N 1000000
#define P 7
#define CODE_LENGTH 32
#define SIR -10
#define NUM 0

static unsigned long seed = 1;

#include "rnd.h"
#include "nrnd.h"

void MakeMyData(double* InputData, double* pn, double* OutputData);
void MakeOtherData(double* InputData, double* pn, double* OutputData);
